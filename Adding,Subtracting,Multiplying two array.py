import numpy as np
array1 = np.array([10, 20, 30, 40])
array2 = np.array([2, 4, 6, 8])
print("Initial Arrays:")
print("Array 1:", array1)
print("Array 2:", array2)
print("-" * 30)
add_operator = array1 + array2
print("Addition (+):      ", add_operator)
sub_operator = array1 - array2
print("Subtraction (-):   ", sub_operator)
mul_operator = array1 * array2
print("Multiplication (*):", mul_operator)
print("-" * 30)
